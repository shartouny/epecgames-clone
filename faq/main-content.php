                <h1 dir="ltr" style="text-align:center"><span style="font-size:24px"><strong>Frequently Asked Questions</strong></span></h1>
                <span style="font-size:14px"><em>Last Update: November 20, 2020</em></span><br>
                &nbsp;
                <p dir="ltr"><span style="font-size:14px"><strong>What is the Epic Games Store? </strong><br>
                        The Epic Games Store is a curated digital storefront for PC and Mac, designed with both players and creators in mind. It’s focused on providing great games for gamers, and a fair deal for game developers. When you buy a game on the Epic Games Store, 88% of the price goes directly to developers, versus only 70% on many other stores. This helps developers invest into building bigger and better games.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>Where can I download the Epic Games Store?</strong><br>
                        Visit the Epic Games Store page at <a href="https://www.epicgames.com/store">epicgames.com</a>, go to the upper right corner, and click “Get Epic Games.”&nbsp; This is the only place to safely download the installer for the Epic Games Store. Do not trust other sources, as they are likely unsafe.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>Which platforms does the Epic Games Store support?</strong><br>
                        The Epic Games Store currently offers PC and Mac support. You can check platform compatibility for individual titles by referring to the “About Game” section of any product page.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>What are the future plans for the Epic Games Store? </strong><br>
                        You can find upcoming features, developer updates, and major known issues on our <a href="https://trello.com/b/GXLc34hk/epic-games-store-roadmap">Epic Games Store Roadmap</a> on Trello. We’ll also share significant updates with you on our <a href="https://www.epicgames.com/store/news">news feed</a> and social media pages such as <a href="https://www.facebook.com/epicgames">Facebook</a>, <a href="https://twitter.com/EpicGames">Twitter</a>, <a href="https://www.instagram.com/epicgames/">Instagram</a>, and <a href="https://www.youtube.com/channel/UC5Qk8mWBwtMyEj7iQQYRk1A">YouTube</a>.&nbsp;<br>
                        <br>
                        <strong>Why does the Epic Games Store make exclusivity deals?&nbsp;</strong><br>
                        Exclusives are a part of the growth of many successful platforms for games and for other forms of digital entertainment, such as streaming video and music.</span></p>

                <p dir="ltr"><span style="font-size:14px">Epic works in partnership with developers and publishers to offer games exclusively on the store. In exchange for exclusivity, Epic provides them with financial support for development and marketing, which enables them to build more polished games with significantly less uncertainty for the creators.</span></p>

                <p dir="ltr"><span style="font-size:14px">In addition, creators will earn 88% of all the revenue from their game, while most stores only offer 70%.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>What is the Support-A-Creator program?&nbsp;</strong><br>
                        The Support-A-Creator program enables content Creators to earn money from games in the Epic Games Store by using Creator Links and Creator Tags. Learn more about the Support-A-Creator program <a href="https://www.epicgames.com/affiliate/en-US/overview">here</a>.&nbsp;</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>What’s this about free games?</strong><br>
                        Epic will be offering a new free game available each week throughout 2020. When you claim a free game, it’s yours to keep - even after the game is no longer available to new customers for free.<br>
                        <br>
                        <strong>I claimed a free game but don’t see it on my account now, why?</strong><br>
                        Once you claim a free game, it’s yours to keep. If you come back later and don’t see it your account, please check to see if you have multiple accounts. If you created an Epic account using an @gmail.com email address, log in to it directly using your Gmail password; using the Google login button will create a distinct account even if it’s tied to the same @gmail.com email address. And check to see if you have both a console-linked account (logging in via PlayStation, Xbox, or Nintendo account) and a separate Epic account. If you still encounter issues, please contact player support <a href="https://epicgames.helpshift.com/a/epic-games-store-and-launcher/?contact=1">here</a>.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>Can I try a game before I buy it?</strong><br>
                        Some publishers occasionally offer demos or free trial periods for certain non-free games from time to time (for example, a Free Weekend trial). During a free trial period, you can download and play a trial version of the game before you decide to purchase, but you can no longer access the game when the trial period ends.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>How do refunds work on the Epic Games Store? </strong><br>
                        All games are eligible for refund within 14 days of purchase for any reason, as long as you’ve had the game running for less than 2 hours. You will not be eligible for refunds for games from which you have been banned or for which you have otherwise violated the Terms of Service. Learn more about our refund policy <a href="https://www.epicgames.com/site/en-US/store-refund-policy">here</a>.&nbsp;</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>How do I contact support? </strong><br>
                        You can contact our support team <a href="https://epicgames.helpshift.com/a/epic-games-store-and-launcher/?contact=1">here</a>. We also recommend browsing our <a href="https://epicgames.helpshift.com/a/epic-games-store-and-launcher/">support center&nbsp;articles</a>, which may help answer questions or resolve issues.&nbsp;<br>
                        <br>
                        <strong>Is my Epic Games account secure?&nbsp;</strong><br>
                        The Epic account system powers Fortnite, the Epic Games store, and Unreal Engine. This account system has never been compromised. However, specific individual Epic accounts have been compromised by hackers using lists of email addresses and passwords leaked from other sites which have been compromised.&nbsp;</span></p>

                <p dir="ltr"><span style="font-size:14px">If you use the same email address and password on Epic as you used on another site which has been compromised, then your account is vulnerable to attack. To secure your Epic account, use a unique password, and enable multi-factor authentication. You can learn more about the measures we take to protect your account and what you can do to stay safe <a href="https://www.epicgames.com/store/en-US/news/epic-games-account-security">here</a>.</span></p>

                <p dir="ltr"><span style="font-size:14px"><strong>What languages does the Epic Games Store support?</strong><br>
                        The Epic Games Store currently supports English, Arabic, German, Spanish (Spain), Spanish (Latin America), French, Italian, Japanese, Korean, Polish, Portugeuse, Russian, Thai, Turkish, Simplified Chinese, and Traditional Chinese. In-game language support varies per game, as provided by the developer; check each game’s store page for language availability.</span></p>

                <p dir="ltr"><br>
                    <span style="font-size:14px"><strong>Does the Epic Games Store support regional pricing?</strong><br>
                        Yes, we do support regional pricing in more than 190 countries and over 30 territories. We also have a set of suggested regional discounts for developers based on local norms that are regularly reviewed.<br>
                        <br>
                        <strong>Which currencies do you accept and in which currencies do you display prices?</strong><br>
                        The Epic Games Store currently accepts 29&nbsp;currencies (USD, EUR, GBP, PLN, BRL, UAH, RUB, KRW, JPY, TRY, AUD, CAD, DKK, NOK, SEK, CZK, ILS, CHF, MXN, PEN, HUF, CLP, SAR, AED, RON, NZD, ZAR, INR, COP). We’re working to bring more currencies online.</span></p>

                <p dir="ltr"><span style="font-size:14px">If your currency is not on the list, you’ll see the prices displayed in US Dollars, but will still pay in your local currency according to the current exchange rates. In some countries paying in US Dollars may incur additional bank conversion fees. We are working on expanding the list of supported currencies. This list will be updated as we add currency payment options.</span></p>

